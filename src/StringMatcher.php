/**
 * Interface StringMatcher
 */
interface StringMatcher
{
    /**
     * @param string $string
     * @param int $offset
     *
     * @return string|null
     */
    public function match($string, $offset = 0);

    /**
     * @return int|null offset where match was found in subject, null if there was no match
     */
    public function getOffset();
}
